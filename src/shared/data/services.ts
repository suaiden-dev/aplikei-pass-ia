export function getServiceBySlug(slug: string): ServiceMeta | undefined {
  const finalSlug = getCanonicalSlug(slug);
  const base = servicesData.find((s) => s.slug === finalSlug);
  if (!base) return undefined;

  const lang = getCurrentLanguage();
  const localized = getServiceLocale(finalSlug, lang);
  if (!localized) return base;

  return {
    ...base,
    ...localized,
    steps: localized.steps?.length
      ? (base.steps.map((step, index) => ({
          ...step,
          ...(localized.steps?.[index] ?? {}),
        })) as StepConfig[])
      : base.steps,
  };
}

function getCurrentLanguage(): Language {
  if (typeof window === "undefined") return DEFAULT_LANGUAGE;
  const saved = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return saved && isSupportedLanguage(saved) ? saved : DEFAULT_LANGUAGE;
}

export function getCanonicalSlug(slug: string): string {
  const slugMap: Record<string, string> = {
    "visa-b1b2": "visto-b1-b2",
    "visa-b1b2-reaplicacao": "visto-b1-b2-reaplicacao",
    "visa-f1": "visto-f1",
    "visa-f1-reaplicacao": "visto-f1-reaplicacao",
    "visa-eos": "extensao-status",
    "visa-cos": "troca-status",
    "mentoring-bronze": "mentoria-individual",
    "mentoring-silver": "mentoria-bronze",
    "mentoring-gold": "mentoria-gold",
    "mentoria-prata": "mentoria-bronze",
    "consultancy-negative-b1b2": "mentoria-negativa-consular",
    "consultancy-negative-f1": "consultoria-f1-negativa",
  };
  return slugMap[slug] || slug;
}

export function isSameService(slug1: string, slug2: string): boolean {
  if (!slug1 || !slug2) return false;
  return getCanonicalSlug(slug1) === getCanonicalSlug(slug2);
}

export function getServiceSlugs(slug: string): string[] {
  const canonical = getCanonicalSlug(slug);
  const inverseMap: Record<string, string[]> = {
    "visto-b1-b2": ["visa-b1b2"],
    "visto-b1-b2-reaplicacao": ["visa-b1b2-reaplicacao"],
    "visto-f1": ["visa-f1"],
    "visto-f1-reaplicacao": ["visa-f1-reaplicacao"],
    "extensao-status": ["visa-eos"],
    "troca-status": ["visa-cos"],
    "mentoria-individual": ["mentoring-bronze"],
    "mentoria-bronze": ["mentoring-silver"],
    "mentoria-gold": ["mentoring-gold"],
    "mentoria-negativa-consular": ["consultancy-negative-b1b2"],
    "consultoria-f1-negativa": ["consultancy-negative-f1"],
  };
  const aliases = inverseMap[canonical] || [];
  return [canonical, ...aliases];
}
